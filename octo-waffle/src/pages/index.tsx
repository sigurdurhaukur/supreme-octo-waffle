const filePath = path.join(process.cwd(), "word2vec.kv");

// Load the model
async function loadModel(filePath: string): Promise<any> {
  return new Promise((resolve, reject) => {
    w2v.loadModel(filePath, (error: any, model: any) => {
      if (error) {
        reject(error);
      } else {
        resolve(model);
      }
    });
  });
}

